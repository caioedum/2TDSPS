// Exercício - Classificação Numérica
fun main() {
    
    val numero = 7 // Definição do número a ser verificado

    if (numero > 0) { // Verifica se o número é maior que zero

        println("O numero $numero e positivo") // Se verdadeiro vai imprimir isso aqui

    } else if (numero < 0) { // Caso o contrário verifica se o número e menor que zero

        println("O numero $numero e negativo") // Se verdadeiro imprimi isso aqui

    } else {

        println("O numero $numero e zero") // Se nenhuma das condições anteriores forem verdadeiras, print

    }
}